function return_setting_from_customizer( $setting ) {

    $setting_value = null;

    $setting_key  = 'ehven_setcon_' . $setting;
    $option_value = get_option( $setting_key );

    if ( $option_value ) {
        $setting_value = $option_value;
    }

    return $setting_value;

}

